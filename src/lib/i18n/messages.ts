export type MessageKey = keyof typeof en & string;
export type PluralMessageKey = {
  [Key in MessageKey]: (typeof en)[Key] extends PluralMessage ? Key : never;
}[MessageKey];
export type TextMessageKey = Exclude<MessageKey, PluralMessageKey>;
